<?php 
       
       /* O loop do while executa o bloco de codigo pelo menos
          uma vez e em seguida verifica se a condição e verdadeira,
          se sim, o bloco de codigo e executado novamente.
       */
       
$x = 10;

do {
  echo "O numero que esta em X é: $x <br>";
  $x++;
} while ($x <= 5);

?>
